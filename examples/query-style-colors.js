async function main() {
    const files = await CW.getFiles(["./examples/test_styles/**/*.styl"], {
        ignore: ['**/node_modules/**']
    });

    // colors
    const allColors = await R.pipedAsync(
        files,
        CW.concatAll([
            CW.match('rgb(:[[args]]'),
            CW.match('rgba(:[[args]]'),
            CW.match('hsl(:[[args]]'),
            CW.match('hsla(:[[args]]'),
            CW.match('#:[[hexcolor]]'),
            R.pipeAsync(
                CW.concatAll([
                    CW.match('$:[name~[\\w-_]+] = :[[pval]]'),
                    CW.match(':[name~[\\w-_]+]: :[[pval]]')
                ]),
                CW.selectVars(['pval']),
                R.reduce((acc, value) => {
                    const named = NAMED_COLOR.find((color) => value.matched.indexOf(color) > -1);
                    if (named) {
                        acc.push({
                            ...value,
                            matched: named
                        });
                    }
                    const nonStandard = NON_STANDARD_COLOR.find((color) => value.matched.indexOf(color) > -1);
                    if (nonStandard) {
                        acc.push({
                            ...value,
                            matched: nonStandard
                        });
                    }
                    return acc;
                }, [])
            )
        ]),
        R.pluck('matched'),
        R.uniq,
        R.sortBy(R.identity)
    );

    console.log('\n== colors ===')
    console.log(CW.toYamlString(allColors));
}

main();
